function setLanguage(lang) {
  const dictionary = translations[lang] || translations.ko;
  document.documentElement.lang = lang;
  localStorage.setItem("hyroxsim-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const text = dictionary[key];
    if (text) {
      node.textContent = text;
    }
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    button.classList.toggle("is-active", button.getAttribute("data-lang-button") === lang);
  });
}

document.querySelectorAll("[data-lang-button]").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.getAttribute("data-lang-button"));
  });
});

const savedLanguage = localStorage.getItem("hyroxsim-lang");
const browserLanguage = navigator.language && navigator.language.toLowerCase().startsWith("ko") ? "ko" : "en";
setLanguage(savedLanguage || browserLanguage || "ko");

const yearNode = document.getElementById("year");
if (yearNode) {
  yearNode.textContent = String(new Date().getFullYear());
}
